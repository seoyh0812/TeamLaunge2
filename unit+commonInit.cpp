#include "stdafx.h"
#include "unit.h"

void unit::commonInit()
{
	// int
	_frameCount = _frame = _delay = _frameDirection = _deathDuration = 0;
	_target = _tileNum = -1; // 없는 상태면 -1
	// float
	_angle = 0.f;
	// bool
	_attackReady = _erase = false;
	_state = ATTACKWAIT; // 이거 안하면 처음에 WALK로 안하니 이미지를 못넣어서 터짐	
	setState(WALK);		// 셋스테이트때매 워크로 바뀌니 놀라지말것..

	_rc = RectMakeCenter(_x, _y, _width, _height);
	_destX = _x; _destY = _y;
	_HP = _maxHP;
}